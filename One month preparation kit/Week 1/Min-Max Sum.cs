class Result
{

    /*
     * Complete the 'miniMaxSum' function below.
     *
     * The function accepts INTEGER_ARRAY arr as parameter.
     */

    public static void miniMaxSum(List<int> arr)
    {
        long high = arr.Max(); // Get the highest value in arr.
        long low = arr.Min(); // Get the lowest value in arr.
        long total = 0; // Initialize variable that will store the sum as we iterate below.
        arr.ForEach(x => total += x); //Sum() here took too long as opposed to iterating through this arr
        long minSum = total - high;
        long maxSum = total - low;
        
        Console.WriteLine($"{minSum} {maxSum}");
    }

}

class Solution
{
    public static void Main(string[] args)
    {

        List<int> arr = Console.ReadLine().TrimEnd().Split(' ').ToList().Select(arrTemp => Convert.ToInt32(arrTemp)).ToList();

        Result.miniMaxSum(arr);
    }
}
